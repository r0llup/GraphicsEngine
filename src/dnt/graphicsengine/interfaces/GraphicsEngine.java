package dnt.graphicsengine.interfaces;

/**
 * Declaration of a GraphicsEngine
 * @author Sh1fT
 *
 */
public interface GraphicsEngine {
	public SceneManager getSceneManager();
	public Camera getCamera();
	public Light getLight();
	public Scene getScene();
	/**
	 * Turn on the graphics engine
	 */
	public void turnOn();
	/**
	 * Turn off the graphics engine
	 */
	public void turnOff();
	/**
	 * Check if the graphics engine is turned on
	 * @return
	 */
	public Boolean isTurnedOn();
	/**
	 * Check if the graphics engine is turned off
	 * @return
	 */
	public Boolean isTurnedOff();
	/**
	 * Initialize the graphics engine
	 */
	public void initialize();
}