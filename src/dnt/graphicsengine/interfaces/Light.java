package dnt.graphicsengine.interfaces;

/**
 * Declaration of a Light
 * @author Sh1fT
 *
 */
public interface Light {
	/**
	 * Turn on the light
	 */
	public void turnOn();
	/**
	 * Turn off the light
	 */
	public void turnOff();
	/**
	 * Check if the light is turned on
	 * @return
	 */
	public Boolean isTurnedOn();
	/**
	 * Check if the light is turned off
	 * @return
	 */
	public Boolean isTurnedOff();
}