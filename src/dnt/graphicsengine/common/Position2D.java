package dnt.graphicsengine.common;

/**
 * Encapsulate a couple of x;y coordinates
 * @author Sh1fT
 *
 */
public class Position2D {
	private Double x;
	private Double y;

	/**
	 * Create a new Position2D instance
	 */
	public Position2D() {
		this.setX(0d);
		this.setY(0d);
	}

	/**
	 * Create a new Position2D instance
	 * @param x
	 * @param y
	 */
	public Position2D(Double x, Double y) {
		this.setX(x);
		this.setY(y);
	}

	/**
	 * Create a new Position2D instance
	 * @param position2D
	 */
	public Position2D(Position2D position2D) {
		this.setX(position2D.getX());
		this.setY(position2D.getY());
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}
}