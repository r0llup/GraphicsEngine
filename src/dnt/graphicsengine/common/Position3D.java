package dnt.graphicsengine.common;

/**
 * Encapsulate a couple of x;y;z coordinates
 * @author Sh1fT
 *
 */
public class Position3D extends Position2D {
	private Double z;

	/**
	 * Create a new Position3D instance
	 */
	public Position3D() {
		super();
		this.setZ(0d);
	}

	/**
	 * Create a new Position3D instance
	 * @param x
	 * @param y
	 * @param z
	 */
	public Position3D(Double x, Double y, Double z) {
		super(x, y);
		this.setZ(z);
	}

	/**
	 * Create a new Position3D instance
	 * @param position3D
	 */
	public Position3D(Position3D position3D) {
		super(position3D);
		this.setZ(position3D.getZ());
	}

	public Double getZ() {
		return z;
	}

	public void setZ(Double z) {
		this.z = z;
	}
}